# Example Variables
name = "Yasir"          # String
age = 21                # Integer
height = 5.11           # Float
is_student = True       # Boolean

# Printing variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Student:", is_student)

# Updating a variable
age = age + 1
print("Next year, I will be:", age)

# Combining text and variables
print(f"Hello, my name is {name} and I am {age} years old.")
